// Array of fruits
let fruits = ["Apple", "Banana", "Orange", "Grapes", "Mango", "Tomato"];

// Access the third fruit
console.log(fruits[2]); // "Orange"

// Update "Grapes" to "Strawberry" and add a new fruit
fruits[3] = "Strawberry";
fruits.push("Pineapple");

console.log(fruits);
// ["Apple", "Banana", "Orange", "Strawberry", "Mango", "Pineapple"]
